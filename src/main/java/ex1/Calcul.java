package ex1;

/**
 * calcul
 */
public class Calcul {

    /**
     * get add
     *
     * @param a a
     * @param b b
     * @return {@link int}
     */
    public int getAdd(int a, int b) {
        return a + b;
    }

    /**
     * get sub
     *
     * @param a a
     * @param b b
     * @return {@link int}
     */
    public int getSub(int a, int b) {
        return a - b;
    }
}
